  const cleanFileName = `${resourceId}-${issueId}.md`.replace(/[^a-zA-Z0-9\-\.]/g, "_");
  const filePath = path.join(fixDir, cleanFileName);

  fs.writeFileSync(filePath, fixText, "utf8");

  console.log(`\nğŸ“„ Fix saved: ${filePath}`);

